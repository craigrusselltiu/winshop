export function getProductBySlug(slug) {
  return products.find((p) => p.slug === slug) || null
}

export function getProductsByCategory(category) {
  if (!category || category === 'all') return products
  if (category === 'on-sale') return products.filter((p) => p.onSale)
  if (category === 'pre-orders')
    return products.filter((p) => p.category === 'pre-order')
  return products.filter((p) => p.category === category)
}

export function getFeaturedProducts() {
  return featuredSlugs
    .map((slug) => getProductBySlug(slug))
    .filter(Boolean)
}

export function sortProducts(items, sortBy) {
  const sorted = [...items]
  switch (sortBy) {
    case 'price-asc':
      return sorted.sort((a, b) => a.basePrice - b.basePrice)
    case 'price-desc':
      return sorted.sort((a, b) => b.basePrice - a.basePrice)
    case 'name-asc':
      return sorted.sort((a, b) => a.name.localeCompare(b.name))
    case 'featured':
    default:
      return sorted.sort((a, b) => a.sortOrder - b.sortOrder)
  }
}
